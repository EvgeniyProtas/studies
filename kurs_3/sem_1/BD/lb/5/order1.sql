select `Наименование`, `Спецификация`, `ДатаПоставки`,`приходсклад`.`Количество`
		from приходсклад
		join `склад` on `склад`.`Склад`=`приходсклад`.`СкладID`
		join `каталогтоваров` on `каталогтоваров`.`КодТовара`=`склад`.`КодТовара`	and `каталогтоваров`.`Наименование`='Монитор'
