select `Наименование`, `Спецификация`, `ДатаПоставки`,`приходсклад`.`Количество`,`организации`.`Название`
		from приходсклад
		join `склад` on `склад`.`Склад`=`приходсклад`.`СкладID`
		join `каталогтоваров` on `каталогтоваров`.`КодТовара`=`склад`.`КодТовара`	and `каталогтоваров`.`Наименование`='Телевизор'
		join `поставщики` on `поставщики`.`МенеджерПоставщикаID`=`приходсклад`.`КодПоставщика`
		join `организации` on `организации`.`ОрганизацииID`=`поставщики`.`ПоставщикID` and `организации`.`Название`='УП "Белкантон"' ;

