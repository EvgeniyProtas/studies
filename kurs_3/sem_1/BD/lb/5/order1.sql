select `клиенты`.`Менеджер`, 
		group_concat(`заказы`.`ЗаказID` order by `клиенты`.`Менеджер`  SEPARATOR ', ') as 'заказID'
		from `клиенты`
		join `заказы` on `клиенты`.`МенеджерКлиентаID`=`заказы`.`МенеджерКлиентаID`
		group by `клиенты`.`Менеджер`;

