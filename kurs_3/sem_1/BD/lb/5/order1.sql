select   `ДатаЗаказа`,
			`каталогтоваров`.`Наименование`,
			`Спецификация`,
			`заказанотоваров`.`Количество` as 'заказано товаров',
			`ЦенаПродажи`,
			`ЦенаОтпускная`,
			`ОбщаяСумма`,
			`Состояние`,
			`заказы`.`НомерНакладной`,
			`организации`.`Название` as 'заказчик',
			`сотрудники`.`ФИО` as 'сотрудник'			
			
			
		from `заказы`
		join `состояниезаказа` on `состояниезаказа`.`СостояниеID`=`заказы`.`СостояниеID`
		join `заказанотоваров` on `заказанотоваров`.`ЗаказID`=`заказы`.`ЗаказID`
		join `склад` on `склад`.`Склад`=`заказанотоваров`.`СкладID`
		join `каталогтоваров` on `каталогтоваров`.`КодТовара`=`склад`.`КодТовара`			
		join `сотрудники` on `сотрудники`.`СотрудникID`=`заказы`.`СотрудникID`
		join `клиенты` on `клиенты`.`МенеджерКлиентаID`=`заказы`.`МенеджерКлиентаID`		
		join `организации` on `организации`.`ОрганизацииID`=`клиенты`.`КлиентID` 
		where `заказы`.`ЗаказID`=13;
		
